#pragma once
#include "Mesh.hpp"

namespace fe::Primitives {

inline Mesh GenerateCube(float size = 1.0f) {
    float h = size * 0.5f;

    std::vector<Vertex> vertices = {
        // +X face
        {{ h,-h,-h}, {1,0,0}, {0,0}},
        {{ h, h,-h}, {1,0,0}, {0,1}},
        {{ h, h, h}, {1,0,0}, {1,1}},
        {{ h,-h, h}, {1,0,0}, {1,0}},

        // -X face
        {{-h,-h, h}, {-1,0,0}, {0,0}},
        {{-h, h, h}, {-1,0,0}, {0,1}},
        {{-h, h,-h}, {-1,0,0}, {1,1}},
        {{-h,-h,-h}, {-1,0,0}, {1,0}},

        // +Y face
        {{-h, h,-h}, {0,1,0}, {0,0}},
        {{-h, h, h}, {0,1,0}, {0,1}},
        {{ h, h, h}, {0,1,0}, {1,1}},
        {{ h, h,-h}, {0,1,0}, {1,0}},

        // -Y face
        {{-h,-h, h}, {0,-1,0}, {0,0}},
        {{-h,-h,-h}, {0,-1,0}, {0,1}},
        {{ h,-h,-h}, {0,-1,0}, {1,1}},
        {{ h,-h, h}, {0,-1,0}, {1,0}},

        // +Z face
        {{-h,-h, h}, {0,0,1}, {0,0}},
        {{ h,-h, h}, {0,0,1}, {0,1}},
        {{ h, h, h}, {0,0,1}, {1,1}},
        {{-h, h, h}, {0,0,1}, {1,0}},

        // -Z face
        {{ h,-h,-h}, {0,0,-1}, {0,0}},
        {{-h,-h,-h}, {0,0,-1}, {0,1}},
        {{-h, h,-h}, {0,0,-1}, {1,1}},
        {{ h, h,-h}, {0,0,-1}, {1,0}},
    };

    std::vector<uint32_t> indices = {
        // +X
        0, 1, 2,   0, 2, 3,
        // -X
        4, 5, 6,   4, 6, 7,
        // +Y
        8, 9, 10,  8, 10, 11,
        // -Y
        12, 13, 14, 12, 14, 15,
        // +Z
        16, 17, 18, 16, 18, 19,
        // -Z
        20, 21, 22, 20, 22, 23,
    };

    return Mesh(vertices, indices);
}

} // namespace fe::Primitives